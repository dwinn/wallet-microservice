package com.wallet.demo.models.enums;

/**
 * Enum representing the different types of transaction that can be made.
 *
 * @author David Winn
 */
public enum TransactionType {

    /**
     * A credit transaction is when a player adds money to their account.
     */
    CREDIT,

    /**
     * A debit transaction is when a player takes money from their account.
     */
    DEBIT
}
